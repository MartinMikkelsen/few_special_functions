/// Horner's method: evaluates c[0] + c[1]*x + c[2]*x² + ... + c[n]*xⁿ
fn horner(x: f64, coeffs: &[f64]) -> f64 {
    coeffs.iter().rev().fold(0.0, |acc, &c| acc * x + c)
}

fn rational_approx(j: f64, x: f64, a_low: &[f64], b_low: &[f64], a_high: &[f64], b_high: &[f64]) -> f64 {
    if x < 2.0 {
        let t = x.exp();
        t * horner(t, a_low) / horner(t, b_low)
    } else {
        let t = 1.0 / (x * x);
        x.powf(j + 1.0) * horner(t, a_high) / horner(t, b_high)
    }
}

fn general_approx(j: f64, x: f64) -> f64 {
    // Aymerich-Humet et al. (1983), eqs. (6)-(7)
    let a = (1.0 + 15.0 / 4.0 * (j + 1.0) + (j + 1.0).powi(2) / 40.0).sqrt();
    let b = 1.8 + 0.61 * j;
    let c = 2.0 + (2.0 - 2.0_f64.sqrt()) * 2.0_f64.powf(-j);
    let inner = ((x - b).abs().powf(c) + a.powf(c)).powf(1.0 / c);
    ((j + 1.0) * 2.0_f64.powf(j + 1.0) / (b + x + inner).powf(j + 1.0)
        + (-x).exp() / libm::tgamma(j + 1.0))
    .recip()
}

/// Complete Fermi-Dirac integral F_j(x) = ∫₀^∞ tʲ / (exp(t-x) + 1) dt
///
/// Uses Antia (1993) rational approximations for j = -1/2, 1/2, 3/2, 5/2,
/// exact formula for j = 0, and Aymerich-Humet et al. (1983) otherwise.
///
/// # Panics
/// Panics if `j < -0.5`.
pub fn fermi_dirac_integral(j: f64, x: f64) -> f64 {
    assert!(j >= -0.5, "j must be >= -0.5, got {j}");

    if j == 0.0 {
        (1.0 + x.exp()).ln()
    } else if j == -0.5 {
        rational_approx(j, x, &A_NEG_HALF_LOW, &B_NEG_HALF_LOW, &A_NEG_HALF_HIGH, &B_NEG_HALF_HIGH)
    } else if j == 0.5 {
        rational_approx(j, x, &A_HALF_LOW, &B_HALF_LOW, &A_HALF_HIGH, &B_HALF_HIGH)
    } else if j == 1.5 {
        rational_approx(j, x, &A_THREE_HALF_LOW, &B_THREE_HALF_LOW, &A_THREE_HALF_HIGH, &B_THREE_HALF_HIGH)
    } else if j == 2.5 {
        rational_approx(j, x, &A_FIVE_HALF_LOW, &B_FIVE_HALF_LOW, &A_FIVE_HALF_HIGH, &B_FIVE_HALF_HIGH)
    } else {
        general_approx(j, x)
    }
}

/// Normalised Fermi-Dirac integral F_j(x) / Γ(j+1)
pub fn fermi_dirac_integral_norm(j: f64, x: f64) -> f64 {
    fermi_dirac_integral(j, x) / libm::tgamma(j + 1.0)
}

